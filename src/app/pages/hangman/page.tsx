const letters = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";

export default function Hangman() {
  const [revealed, setRevealed] = useState(false);
  const [guesses, setGuesses] = useState(0);
  const [currWord, setCurrWord] = useState("");
  const [currGuess, setCurrGuess] = useState("");
  const [maxGuesses, setMaxGuesses] = useState(8);
  const [currDisplayWord, setCurrDisplayWord] = useState("");
  function replaceAt(str, index, replacement) {
    //helpful function to replace a character at a specific index
    return str.substring(0, index) + replacement + str.substring(index + 1);
  }
  function handleGuess(letter) {
    if (currGuess.includes(letter)) return;
    if (!currWord.toUpperCase().includes(letter.toUpperCase())) {
      setGuesses(guesses + 1);
    }
    setCurrGuess(currGuess + letter); // Updates total guesses made by the user

    let nextDisplay = currDisplayWord;
    for (let i = 0; i < currWord.length; i++) {
      if (currWord[i].toUpperCase() === letter.toUpperCase()) {
        nextDisplay = replaceAt(nextDisplay, i, letter);
      }
    }
    setCurrDisplayWord(nextDisplay);

    if (guesses >= maxGuesses) {
      setCurrGuess(letters);
      alert("You Lost!");
      return;
    }
    if (nextDisplay.toUpperCase() === currWord.toUpperCase()) {
      setCurrGuess(letters);
      alert("You Won!");
      return;
    }
  }

  function handleReset() {
    let randomWord = words[Math.floor(Math.random() * words.length)];
    setCurrWord(randomWord);
    setCurrDisplayWord(randomWord.replace(/[a-zA-z]/gi, "_"));
    setGuesses(0);
    setMaxGuesses(8);
    setCurrGuess("");
  }

  function toggleVisible() {
    setRevealed(!revealed);
  }

  return (
    <div>
      <div className="flex flex-col items-center justify-center">
        {" "}
        {/* Header */}
        <h1 className="text-4xl font-bold">Hangman</h1>
        <p className="text-xl">Guess the word!</p>
      </div>
      <div className="flex flex-col items-center justify-center w-full">
        {" "}
        {/* Game Board */}
        <div className="flex flex-col items-center justify-center w-full">
          <div className="flex flex-col gap-5 items-center justify-center w-1/2 my-5">
            <p className="tracking-widest text-3xl mx-auto">
              {currDisplayWord}
            </p>

            {revealed ? <p className="text-2xl">{currWord}</p> : null}
          </div>
          <div className="grid grid-cols-4 sm:grid-cols-9 grid-rows-auto gap-1 sm:gap-3 items-center justify-center sm:p-2 p-4 w-full sm:w-3/4">
            {currWord !== ""
              ? letters.split("").map((letter, index) => (
                  <Button
                    variant={
                      currGuess.includes(letter.toUpperCase())
                        ? "disabled"
                        : "default"
                    }
                    key={index}
                    onClick={() => handleGuess(letter)}
                    className="w-full"
                  >
                    {letter}
                  </Button>
                ))
              : null}
          </div>
        </div>
      </div>
      <div className="flex flex-col gap-5 items-center justify-center w-full mt-4">
      
        {/* Buttons */}
        <div className="flex flex-col items-center text-xl justify-center w-full mt-4">
          {guesses <= maxGuesses ? (
            <p>
              Total Guesses Left: {guesses}/{maxGuesses}
            </p>
          ) : (
            "Game Over!"
          )}
          <p className="text-sm">Word Length: {currWord.length}</p>
        </div>
        <Button onClick={handleReset}>Start New Game</Button>
        <Button onClick={toggleVisible}>Toggle Cheat mode</Button>
        
      </div>
      <div className="flex flex-col items-center text-xl justify-center w-full p-5">
        {" "}
        {/* Rules */}
        <h3 className="text-2xl font-bold">Rules</h3>
        <ul className="list-disc ml-5">
          <li>Guess the letters of the word</li>
          <li>The letters must be in the correct order</li>
          <li>You have 8 guesses. Correct guesses do not count!</li>
          <li>If you guess the word, you win!</li>
        </ul>

        <p className="my-10">Originally made in PHP by myself and student. This was then rewritten in TSX by myself!</p> 
      </div>
    </div>
  );
}
